package Mathematics;

public class Four_Transactions{
    public int gather(int a, int b){
        return a+b;
    }
    public int subtract(int a, int b){
        return a-b;
    }
    public int impact(int a, int b){
        return a*b;
    }
    public int divide(int a, int b){
        return a-b;
    }

}